package com.farm.model;

public class FarmFarmer {
	private String fName;
	private String fEmail;
	private int fMobileNo;
	private String fPassword;
	private FarmAddress fAddr;
	private int fArea;
	private String fAddress;
	private int fZip;
	private FarmBank fBank;
	
	public String getfName() {
		return fName;
	}
	public void setfName(String fName) {
		this.fName = fName;
	}
	public String getfEmail() {
		return fEmail;
	}
	public void setfEmail(String fEmail) {
		this.fEmail = fEmail;
	}
	public int getfMobileNo() {
		return fMobileNo;
	}
	public void setfMobileNo(int fMobileNo) {
		this.fMobileNo = fMobileNo;
	}
	public String getfPassword() {
		return fPassword;
	}
	public void setfPassword(String fPassword) {
		this.fPassword = fPassword;
	}
	public FarmAddress getfAddr() {
		return fAddr;
	}
	public void setfAddr(FarmAddress fAddr) {
		this.fAddr = fAddr;
	}
	public int getfArea() {
		return fArea;
	}
	public void setfArea(int fArea) {
		this.fArea = fArea;
	}
	public String getfAddress() {
		return fAddress;
	}
	public void setfAddress(String fAddress) {
		this.fAddress = fAddress;
	}
	public int getfZip() {
		return fZip;
	}
	public void setfZip(int fZip) {
		this.fZip = fZip;
	}
	public FarmBank getfBank() {
		return fBank;
	}
	public void setfBank(FarmBank fBank) {
		this.fBank = fBank;
	}
	
}
